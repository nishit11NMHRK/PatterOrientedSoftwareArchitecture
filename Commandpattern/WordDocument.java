package Commandpattern;

public class WordDocument
{
	
	
	
	void openApp()
	{
		System.out.println("Open the Application");
	}
	void saveApp()
	{
		System.out.println("Save the Application");
	}
	void closeApp()
	{
		System.out.println("Close the Application");
	}
	
	void undoOpen()
	{
		System.out.println("Undo Open the Application");
	}
	
	void undoSave()
	{
		System.out.println("Undo Save the Application");

	}
	
	void undoClose()
	{
		System.out.println("Undo Close the Application");

	}

}
